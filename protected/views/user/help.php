<p class="size4">What is CSchedule?
 </p>
<p>CSchedule is an app for people to organize scheduled events or activities. It can share schedules among participants and send out reminders (email or push notification on the iPhone) to those on the schedule.</p>
 
<p class="size4">How do I use CSchedule?</p>
 
<p>If you're the organizer of an activity:</p>
 
<p>Step #1: Enter your contacts or import them from your email account or a file.<br/>
Step #2: Create an activity you want to organize.<br/>
Step #3: Share it with the contacts you entered or imported in step #1 via the "Share" menu option on the activity. You can also assign the role "Organizer" to a contact so that he/she can also modify the details of an event or activity. <br/>
Step #4: Create schedules for your activity and assign shared contacts to the activity.</p>
 
<p>If you're the participant of an activity,</p>
 
<p>Step #1: Browse your activities and schedules to see which activities you are a part of.  
<br/>
Step #2: Communicate with other participants of an activity via email, phone call or text message.</p>
 
<p class="size4">How can I help support CSchedule?</p>
 
<p>The best thing you can do is to share CSchedule with anyone you know and to help spread the word. We highly value any feedback from our users. Please use the feedback page at CSchedule.com or visit our Facebook page(search for CSchedule in Facebook) to contact us. We encourage you to write us with any questions, bugs you found while working with CSchedule, or suggestions. Your comments are very important to us.</p>
<p class="size4">What does CSchedule stand for?</p>
 
<p>CSchedule stands for Collaborative Schedule. </p></div>


